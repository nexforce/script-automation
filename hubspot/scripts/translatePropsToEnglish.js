const func = async () => {
  const hubspotClient = new hubspot.Client({
    accessToken: process.env.accessToken,
  });

  const objectTypesArr = [
    "0-1", // Contact
    "0-2", // Company
    "0-3", // Deal
    // "0-5", // Ticket
  ];

  try {
    for (const objectType of objectTypesArr) {
      const allObjectTypeProperties =
        await hubspotClient.crm.properties.coreApi.getAll(objectType);

      const translationData = translations[objectType];

      let i = 0;

      const translatedKeys = [];

      for (const property of allObjectTypeProperties.results) {
        i++;

        const labelTranslation = Object.entries(translationData).find(
          ([_, value]) => value.toLowerCase() === property.label.toLowerCase()
        )?.[0];

        if (labelTranslation && !property.hubspotDefined) {
          await hubspotClient.crm.properties.coreApi.update(
            objectType,
            property.name,
            { label: labelTranslation }
          );

          console.log(
            `${objectType}: ${i}/${allObjectTypeProperties.results.length} âœ… Propriedade com nome "${property.label}" traduzida para "${labelTranslation}"`
          );

          translatedKeys.push(property.label.toLowerCase());
        } else {
          // console.log(
          //   `${objectType}: ${++i}/${
          //     allObjectTypeProperties.results.length
          //   } ðŸŸ¡ TraduÃ§Ã£o nÃ£o encontrada para "${property.label}"`
          // );
        }
      }

      const notTranslatedKeys = {};

      const newTranslations = await hubspotClient.crm.properties.coreApi.getAll(
        objectType
      );

      for (const [translation, property] of Object.entries(translationData)) {
        if (
          !translatedKeys.some((k) => k === property.toLowerCase()) &&
          !newTranslations.results.some(
            (p) => p.label.toLowerCase() === translation.toLowerCase()
          )
        ) {
          notTranslatedKeys[property] = translation;
        }
      }

      fs.writeFileSync(
        `${objectType}-en-not-translated.json`,
        JSON.stringify(notTranslatedKeys, null, 2)
      );
    }
  } catch (err) {
    console.error(err);

    throw err;
  }
};

func();
